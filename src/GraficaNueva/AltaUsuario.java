    private void CrearCuentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CrearCuentaActionPerformed
        // TODO add your handling code here:
        cedula = txtCedula.getText();
        nombre = txtNombre.getText();
        apellido = txtApellido.getText();
        celular = txtCelular.getText();
        String password = cedula;

        try {
            Fachada.getInstancia().alta_usuario(cedula, nombre, apellido, password, celular);
            tbUsuarios.setModel(Fachada.getInstancia().listar_usuarios_tabla());
            JOptionPane.showMessageDialog(null, "Usuario creado con Ã©xito");

        } catch (IOException | SQLException ex) {
            Logger.getLogger(FrmUsuario.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_CrearCuentaActionPerformed

