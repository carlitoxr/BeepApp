    private void tbUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbUsuariosMouseClicked
        // TODO add your handling code here:
        JTable src = (JTable) evt.getSource();
        int row = src.rowAtPoint(evt.getPoint());

        //String col[] = {"Cédula", "Nombre", "Apellido", "Celular"};
        cedula = String.valueOf(tbUsuarios.getValueAt(row, 0));
        nombre = String.valueOf(tbUsuarios.getValueAt(row, 1));
        apellido = String.valueOf(tbUsuarios.getValueAt(row, 2));
        celular = String.valueOf(tbUsuarios.getValueAt(row, 3));

        txtCedula.setText(cedula);
        txtNombre.setText(nombre);
        txtApellido.setText(apellido);
        txtCelular.setText(celular);
    }//GEN-LAST:event_tbUsuariosMouseClicked

    private void btnBajaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBajaActionPerformed
        try {
            // TODO add your handling code here:
            Fachada.getInstancia().baja_usuario(cedula);
            tbUsuarios.setModel(Fachada.getInstancia().listar_usuarios_tabla());
        } catch (IOException | SQLException ex) {
            Logger.getLogger(FrmUsuario.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnBajaActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        try {
            // TODO add your handling code here:
            nombre = txtNombre.getText();
            apellido = txtApellido.getText();
            celular = txtCelular.getText();

            Fachada.getInstancia().modif_usuario(cedula, nombre, apellido, cedula, celular);
            tbUsuarios.setModel(Fachada.getInstancia().listar_usuarios_tabla());
            JOptionPane.showMessageDialog(null, "Los cambios se realizaron con éxito");
        } catch (IOException | SQLException ex) {
            Logger.getLogger(FrmUsuario.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnModificarActionPerformed

